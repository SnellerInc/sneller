type testCompressionStatus struct {
	ionSize    int
	outputSize int
}

func testFloat64Compression(t *testing.T, name string, vec []float64) testCompressionStatus {
	var st ion.Symtab
	var buf ion.Buffer
	sym := st.Intern("label")
	buf.BeginStruct(-1)
	buf.BeginField(sym)
	buf.BeginList(-1)
	for i := range vec {
		buf.WriteCanonicalFloat(vec[i])
	}
	buf.EndList()
	buf.EndStruct()

	src, _ := ion.Contents(buf.Bytes())
	comp, ok := compressNumericList(src, nil)
	if !ok {
		t.Fatalf("dataset %s: couldn't compress: %x\n", name, src)
	}

	t.Logf("dataset %s: %d bytes -> %d bytes", name, len(src), len(comp))
	ret, err := decompressNumericList(comp, nil)
	if err != nil {
		t.Fatal(err)
	}
	if !bytes.Equal(ret, src) {
		t.Errorf("didn't decompress identical bytes")
		t.Errorf("input : %x\n", src)
		t.Errorf("output: %x\n", ret)
	}

	return testCompressionStatus{
		ionSize:    len(src),
		outputSize: len(comp),
	}
}

func TestFloat64ListEncoding1(t *testing.T) {
	for _, sample := range f64SampleDataSet {
		status := testFloat64Compression(t, sample.name, sample.data)
		t.Logf("sample=%s ionSize=%d outputSize=%d (%d%%)\n",
			sample.name, status.ionSize, status.outputSize, (status.outputSize*100)/status.ionSize)
	}
}

